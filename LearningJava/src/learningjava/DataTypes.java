public class DataTypes {
    
    public static void main(String[] args){
        //Primitive Java Data Types
        int myNumber = 5; //stores whole numbers (byte, short, long)
        char myLetter = 'a'; //stores single character
        double myDouble = 1.5; //stores fractional numbers (float)
        boolean trueOrFalse = true; //true or false values
        
        //Non-Primitive Java Data Types (Strings, Array, Classes, Interface, etc.)
        String myWords = "Data Types in Java"; //Contains collection of characters
        
        System.out.println("int = " + myNumber);
        System.out.println("char = " + myLetter);
        System.out.println("double = " + myDouble);
        System.out.println("boolean = " + trueOrFalse);
        System.out.println("string = " + myWords);       
    }
}
